import { request } from '@/utils/request';
import { apiVirtualSuccessAccess, useVirtualAccess } from '@/utils/tools';

export async function queryListData(params) {
  if (useVirtualAccess()) {
    const result = await apiVirtualSuccessAccess({
      pageSize: 10,
      total: 645,
      pageNo: 1,
      data: [],
    });

    return result;
  }

  return request('/GoodsLogisticsProcessRequestMessage/List', {
    method: 'POST',
    body: params,
  });
}

/**
 * 重新合计指定目标数据
 * @param {goodsLogisticsProcessRequestMessageId} params 目标标识
 */
export async function reAggregateData(params) {
  if (useVirtualAccess()) {
    const result = await apiVirtualSuccessAccess({
      data: {},
    });

    return result;
  }

  return request('/GoodsLogisticsProcessRequestMessage/reAggregate', {
    method: 'POST',
    body: params,
  });
}

