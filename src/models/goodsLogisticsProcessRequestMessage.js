import { pretreatmentRemotePageListData, pretreatmentRemoteSingleData } from '@/utils/tools';
import {
  queryListData,
  queryGetData,
  reAggregateData,
} from '@/services/goodsLogisticsProcessRequestMessage';

export default {
  namespace: 'goodsLogisticsProcessRequestMessage',

  state: {},

  effects: {
    *list({ payload }, { call, put }) {
      const response = yield call(queryListData, payload);
      yield put({
        type: 'handlePageListData',
        payload: response,
      });
    },
    *get({ payload }, { call, put }) {
      const response = yield call(queryGetData, payload);
      yield put({
        type: 'handleCommonData',
        payload: response,
      });
    },
    *reAggregate({ payload }, { call, put }) {
      const response = yield call(reAggregateData, payload);
      yield put({
        type: 'handleCommonData',
        payload: response,
      });
    },
  },

  reducers: {
    handlePageListData(state, action) {
      const d = action.payload;
      const v = pretreatmentRemotePageListData(d);

      return {
        ...state,
        data: v,
      };
    },
    handleCommonData(state, action) {
      const d = action.payload;
      const v = pretreatmentRemoteSingleData(d);

      return {
        ...state,
        data: v,
      };
    },
  },
};
